package ablazzing.lesson2Peremennie;

public class Variables {
    public static void main(String[] args) {
        // Переменная это именнованная ссылка на какие-то данные (которые мы сами задаём)
        int countApples = 3;  // инициализация - присвоение значения
        // примитивный тип данных - группа типов данных которые приобразуются в двоичный код
        // и переменная хранит значение этого кода
        System.out.println(countApples);
    }
}
